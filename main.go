package main

import (
	"fmt"
)

// Функция для генерации шахматной доски
func generateChessboard(size int) string {
	// Создаем пустую строку для хранения результата
	var board string

	// Два вложенных цикла: внешний — строки, внутренний — столбцы
	for i := 0; i < size; i++ {
		for j := 0; j < size; j++ {
			// Используем правильную проверку для чередования символов
			if (i+j)%2 == 0 {
				board += "▪" // Четные клетки — белые (▪)
			} else {
				board += "#" // Нечетные клетки — черные (#)
			}
		}
		// Добавляем переход на новую строку после каждого ряда
		board += "\n"
	}
	// Возвращаем готовую строку с шахматной доской
	return board
}

func main() {
	// Переменная для хранения размера доски
	var size int
	fmt.Print("Введите размер доски: ") // Просим пользователя ввести размер
	fmt.Scan(&size) // Читаем ввод пользователя

	// Выводим сгенерированную шахматную доску на экран
	fmt.Println(generateChessboard(size))
}